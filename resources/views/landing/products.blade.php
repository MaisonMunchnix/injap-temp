                        <h5 >Function of the Liver</h5>
                     
                        <p>The liver is an essential organ of the body that performs over 500 vital functions. These include removing waste products and foreign substances from the bloodstream, regulating blood sugar levels, and creating essential nutrients. Here are some of its most important functions:
                        </p>
                        <p>Albumin Production: Albumin is a protein that keeps fluids in the bloodstream from leaking into surrounding tissue. It also carries hormones, vitamins, and enzymes through the body.
                        </p>
                        <p>Bile Production: Bile is a fluid that is critical to the digestion and absorption of fats in the small intestine.
                        </p>
                        <p>Filters Blood: All the blood leaving the stomach and intestines passes through the liver, which removes toxins, byproducts, and other harmful substances.
                        </p>
                        <p>Regulates Amino Acids: The production of proteins depend on amino acids. The liver makes sure amino acid levels in the bloodstream remain healthy.
                        </p>
                        <p>Regulates Blood Clotting: Blood clotting coagulants are created using vitamin K, which can only be absorbed with the help of bile, a fluid the liver produces.​
                        </p>
                        <p>Resists Infections: As part of the filtering process, the liver also removes bacteria from the bloodstream. 
                        </p>
                        <p>Stores Vitamins and Minerals: The liver stores significant amounts of vitamins A, D, E, K, and B12, as well as iron and copper.
                        </p>
                        <p>Processes Glucose: The liver removes excess glucose (sugar) from the bloodstream and stores it as glycogen. 
                        </p>

